class Rinnegan(nn.Module):
    def __init__(
        self,
        encoder_embed_dim=768,
        encoder_depth=12,
        encoder_num_heads=12,
        encoder_global_attn_indexes=[2, 5, 8, 11],
        gaze_embed_dim=256,
        image_size=1024,
        vit_patch_size=16,
        downsize_embed=True,
    ):
        super().__init__()

        self.image_size = image_size
        self.image_embedding_size = image_size // vit_patch_size

        self.pe_layer = PositionEmbeddingRandom(gaze_embed_dim // 2)

        self.image_encoder = ImageEncoderViT(
            depth=encoder_depth,
            embed_dim=encoder_embed_dim,
            img_size=image_size,
            mlp_ratio=4,
            norm_layer=partial(nn.LayerNorm, eps=1e-6),
            num_heads=encoder_num_heads,
            patch_size=vit_patch_size,
            qkv_bias=True,
            use_rel_pos=True,
            global_attn_indexes=encoder_global_attn_indexes,
            window_size=14,
            out_chans=prompt_embed_dim,
            downsize_embed=downsize_embed,
        )

        self.gaze_encoder = GazeEncoder(token_dim=gaze_embed_dim, feature_dim=512)

        self.gaze_decoder = GazeDecoder(
            transformer=TwoWayTransformer(
                depth=2,
                embedding_dim=gaze_embed_dim,
                mlp_dim=2048,
                num_heads=8,
            ),
            transformer_dim=gaze_embed_dim,
        )

    def forward(self, sample):
        # Encoding image
        image_tokens = self.image_encoder(sample["image"]) # (b, d, h, w) / b=batch_size, d=token_dim, h=num_h_tokens, w=num_w_tokens

        # Encode gaze
        gaze_tokens, gaze_vec = self.gaze_encoder(sample["heads"], sample["head_bboxes"]) # (b, n, d) / n=num_people

        # Decode gaze
        image_pe = self.pe_layer((self.image_embedding_size, self.image_embedding_size)).unsqueeze(0)
        gaze_heatmap = self.gaze_decoder(image_tokens, image_pe, gaze_tokens)

        return gaze_heatmap

    def postprocess_masks(
        self,
        masks: torch.Tensor,
        input_size: Tuple[int, ...],
        original_size: Tuple[int, ...],
    ) -> torch.Tensor:
        """
        Remove padding and upscale masks to the original image size.

        Arguments:
          masks (torch.Tensor): Batched masks from the mask_decoder,
            in BxCxHxW format.
          input_size (tuple(int, int)): The size of the image input to the
            model, in (H, W) format. Used to remove padding.
          original_size (tuple(int, int)): The original size of the image
            before resizing for input to the model, in (H, W) format.

        Returns:
          (torch.Tensor): Batched masks in BxCxHxW format, where (H, W)
            is given by original_size.
        """
        masks = F.interpolate(
            masks,
            (self.image_encoder.img_size, self.image_encoder.img_size),
            mode="bilinear",
            align_corners=False,
        )
        masks = masks[..., : input_size[0], : input_size[1]]
        masks = F.interpolate(masks, original_size, mode="bilinear", align_corners=False)
        return masks


